function detectInitialLanguage(): string {
  const device = getLocales()[0]?.languageCode ?? 'ca';
  return ['ca', 'es', 'en'].includes(device) ? device : 'ca';
}

i18n.use(initReactI18next).init({
  resources,
  lng: detectInitialLanguage(),
  fallbackLng: 'ca',
  supportedLngs: ['ca', 'es', 'en'],
  interpolation: { escapeValue: false },
});

// Carrega la preferència desada (asíncrona) i la aplica si difereix.
AsyncStorage.getItem(LANG_STORAGE_KEY).then((saved) => {
  if (saved && saved !== i18n.language) i18n.changeLanguage(saved);
});

export async function changeLanguage(code: string) {
  await AsyncStorage.setItem(LANG_STORAGE_KEY, code);
  await i18n.changeLanguage(code);
}

export default i18n;
